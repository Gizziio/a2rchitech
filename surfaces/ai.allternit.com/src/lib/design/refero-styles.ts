function buildDesignMd(
  siteName: string,
  url: string,
  northStar: string,
  colors: Array<{ name: string; hex: string }>,
  fonts: string[],
  colorScheme: string,
): string {
  return [
    `# Brand: ${siteName}`,
    `## North Star`,
    northStar,
    `## Colors`,
    colors.map(c => `- ${c.name}: ${c.hex}`).join("\n"),
    `## Typography`,
    `- fontFamily: "${fonts.slice(0, 3).join(", ")}"`,
    `## Color Scheme`,
    colorScheme,
    `## Reference`,
    url,
  ].join("\n");
}

